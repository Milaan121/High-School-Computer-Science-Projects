function dragElement(elmnt) {
  var pos1 = 0;
  var pos2 = 0;
  var pos3 = 0;
  var pos4 = 0;
  if (document.getElementById(elmnt.id + "header")) {
    document.getElementById(elmnt.id + "header").onmousedown = dragMouseDown;
  } else {
    elmnt.onmousedown = dragMouseDown;
  }

  function dragMouseDown(e) {
    e = e || window.event;
    e.preventDefault();

    pos3 = e.clientX;
    pos4 = e.clientY;
    document.onmouseup = closeDragElement;

    document.onmousemove = elementDrag;

    function elementDrag(e) {
      e = e || window.event;
      e.preventDefault();

      pos1 = pos3 - e.clientX;
      pos2 = pos4 - e.clientY;
      pos3 = e.clientX;
      pos4 = e.clientY;

      elmnt.style.top = elmnt.offsetTop - pos2 + "px";
      elmnt.style.left = elmnt.offsetLeft - pos1 + "px";
    }

    function closeDragElement() {
      document.onmouseup = null;
      document.onmousemove = null;
    }
  }
}

var whiteMove = true;
var blackMove = false;

var whiteTime = 60 * time;
var blackTime = 60 * time;

setInterval(startTime, 1000);

function whiteMoveOver() {
  whiteMove = false;
  blackMove = true;
  whiteTime += 2;
  document.getElementById("blackMoveOver").style.backgroundColor = "lightgreen";
  document.getElementById("whiteMoveOver").style.backgroundColor = "lightgrey";
}

function blackMoveOver() {
  whiteMove = true;
  blackMove = false;
  blackTime += 2;
  document.getElementById("whiteMoveOver").style.backgroundColor = "lightgreen";
  document.getElementById("blackMoveOver").style.backgroundColor = "lightgrey";
}

function startTime() {
  var whiteMinutes = Math.floor(whiteTime / 60);
  var whiteSeconds = whiteTime % 60;
  var whiteSpaceMid = "";
  var whiteSpaceStart = "";

  if (whiteSeconds <= 9) {
    whiteSpaceMid = "0";
  }
  if (whiteMinutes <= 9) {
    whiteSpaceStart = "0";
  }

  var blackMinutes = Math.floor(blackTime / 60);
  var blackSeconds = blackTime % 60;
  var blackSpaceMid = "";
  var blackSpaceStart = "";
  if (blackSeconds <= 9) {
    blackSpaceMid = "0";
  }
  if (blackMinutes <= 9) {
    blackSpaceStart = "0";
  }

  document.getElementById("whiteTime").innerHTML =
    whiteSpaceStart + whiteMinutes + ":" + whiteSpaceMid + whiteSeconds;

  document.getElementById("blackTime").innerHTML =
    blackSpaceStart + blackMinutes + ":" + blackSpaceMid + blackSeconds;

  if (whiteMove) {
    if (whiteTime == 0) {
      document.getElementById("whiteMoveOver").style.backgroundColor = "red";
    }
    if (whiteTime > 0) {
      whiteTime--;
    }
  } else {
    if (blackTime == 0) {
      document.getElementById("blackMoveOver").style.backgroundColor = "red";
    }
    if (blackTime > 0) {
      blackTime--;
    }
  }
}
